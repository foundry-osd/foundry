using Foundry.Core.Models.Configuration;
using Foundry.Core.Services.Configuration;
using Foundry.Services.Configuration;
using Foundry.Services.Localization;
using Microsoft.UI.Xaml;

namespace Foundry.ViewModels;

/// <summary>
/// Backs deployment customization settings that are generated into the Foundry configuration.
/// </summary>
public sealed partial class CustomizationConfigurationViewModel : ObservableObject, IDisposable
{
    private readonly IFoundryConfigurationStateService configurationStateService;
    private readonly IApplicationLocalizationService localizationService;
    private bool isApplyingState = true;
    private bool isSavingState;

    public CustomizationConfigurationViewModel(
        IFoundryConfigurationStateService configurationStateService,
        ILanguageRegistryService languageRegistryService,
        IApplicationLocalizationService localizationService)
    {
        this.configurationStateService = configurationStateService;
        this.localizationService = localizationService;

        InitializeAppxRemovalCatalog();
        InitializeOperatingSystemSelectionOptions(languageRegistryService.GetLanguages());
        RefreshLocalizedText();
        ApplyState(
            configurationStateService.Current.Customization,
            configurationStateService.Current.OperatingSystemSelection);

        localizationService.LanguageChanged += OnLanguageChanged;
        configurationStateService.StateChanged += OnConfigurationStateChanged;
        isApplyingState = false;
    }

    /// <summary>
    /// Gets the maximum computer-name prefix length accepted by Windows naming rules.
    /// </summary>
    public int MachineNamePrefixMaxLength => ComputerNameTemplate.MaxTemplateLength;

    /// <summary>
    /// Gets whether machine naming controls should be enabled.
    /// </summary>
    public bool IsMachineNamingOptionsEnabled => IsMachineNamingEnabled;

    /// <summary>
    /// Gets whether the current machine-name prefix has a validation error.
    /// </summary>
    public bool HasMachineNamePrefixValidationError => !string.IsNullOrWhiteSpace(MachineNamePrefixValidationMessage);

    /// <summary>
    /// Gets the visibility for the machine-name prefix validation message.
    /// </summary>
    public Visibility MachineNamePrefixValidationVisibility => HasMachineNamePrefixValidationError
        ? Visibility.Visible
        : Visibility.Collapsed;

    public string DocumentationUrl => FoundryApplicationInfo.CustomizationDocumentationUrl;

    [ObservableProperty]
    public partial string PageTitle { get; set; }

    [ObservableProperty]
    public partial string PageDescription { get; set; }

    [ObservableProperty]
    public partial string MachineNamingHeader { get; set; }

    [ObservableProperty]
    public partial string MachineNamingDescription { get; set; }

    [ObservableProperty]
    public partial string MachineNamingEnableText { get; set; }

    [ObservableProperty]
    public partial string MachineNamingPrefixLabel { get; set; }

    [ObservableProperty]
    public partial string MachineNamingPrefixDescription { get; set; }

    [ObservableProperty]
    public partial string MachineNamingAutoGenerateText { get; set; }

    [ObservableProperty]
    public partial string MachineNamingAutoGenerateDescription { get; set; }

    [ObservableProperty]
    public partial string MachineNamingAllowManualSuffixEditText { get; set; }

    [ObservableProperty]
    public partial string MachineNamingAllowManualSuffixEditDescription { get; set; }

    [ObservableProperty]
    public partial bool IsMachineNamingExpanded { get; set; }

    [ObservableProperty]
    [NotifyPropertyChangedFor(nameof(IsMachineNamingOptionsEnabled))]
    [NotifyPropertyChangedFor(nameof(MachineNamePrefixValidationMessage))]
    [NotifyPropertyChangedFor(nameof(HasMachineNamePrefixValidationError))]
    [NotifyPropertyChangedFor(nameof(MachineNamePrefixValidationVisibility))]
    public partial bool IsMachineNamingEnabled { get; set; }

    [ObservableProperty]
    [NotifyPropertyChangedFor(nameof(MachineNamePrefixValidationMessage))]
    [NotifyPropertyChangedFor(nameof(HasMachineNamePrefixValidationError))]
    [NotifyPropertyChangedFor(nameof(MachineNamePrefixValidationVisibility))]
    public partial string MachineNamePrefix { get; set; } = string.Empty;

    [ObservableProperty]
    public partial bool MachineNameAutoGenerate { get; set; }

    [ObservableProperty]
    public partial bool AllowManualSuffixEdit { get; set; } = true;

    /// <summary>
    /// Gets the localized validation message for the machine-name prefix.
    /// </summary>
    public string MachineNamePrefixValidationMessage
    {
        get
        {
            if (!IsMachineNamingEnabled || string.IsNullOrWhiteSpace(MachineNamePrefix))
            {
                return string.Empty;
            }

            return ComputerNameTemplate.IsValidPrefix(MachineNamePrefix.Trim())
                ? string.Empty
                : localizationService.GetString("Customization.MachineNamingPrefixValidation");
        }
    }

    /// <summary>
    /// Releases subscriptions to localization and shared configuration state.
    /// </summary>
    public void Dispose()
    {
        localizationService.LanguageChanged -= OnLanguageChanged;
        configurationStateService.StateChanged -= OnConfigurationStateChanged;
        foreach (AppxRemovalItemViewModel item in AppxRemovalCategories.SelectMany(category => category.Items))
        {
            item.PropertyChanged -= OnAppxRemovalItemPropertyChanged;
        }

        foreach (SelectableStringOptionViewModel option in OperatingSystemLanguageOptions
                     .Concat(OperatingSystemReleaseOptions)
                     .Concat(OperatingSystemLicenseChannelOptions)
                     .Concat(OperatingSystemEditionOptions))
        {
            option.PropertyChanged -= OnOperatingSystemSelectionOptionPropertyChanged;
        }
    }

    partial void OnIsMachineNamingEnabledChanged(bool value)
    {
        IsMachineNamingExpanded = value;
        SaveState();
    }

    partial void OnMachineNamePrefixChanged(string value)
    {
        if (isApplyingState)
        {
            return;
        }

        string normalizedPrefix = NormalizePrefix(value);
        if (!string.Equals(value, normalizedPrefix, StringComparison.Ordinal))
        {
            MachineNamePrefix = normalizedPrefix;
            return;
        }

        SaveState();
    }

    partial void OnMachineNameAutoGenerateChanged(bool value)
    {
        SaveState();
    }

    partial void OnAllowManualSuffixEditChanged(bool value)
    {
        SaveState();
    }

    private void ApplyState(CustomizationSettings settings, OperatingSystemSelectionSettings operatingSystemSelection)
    {
        isApplyingState = true;
        try
        {
            ApplyOperatingSystemSelectionState(operatingSystemSelection);
            IsMachineNamingEnabled = settings.MachineNaming.IsEnabled;
            IsMachineNamingExpanded = settings.MachineNaming.IsEnabled;
            MachineNamePrefix = settings.MachineNaming.Prefix ?? string.Empty;
            MachineNameAutoGenerate = settings.MachineNaming.AutoGenerateName;
            AllowManualSuffixEdit = settings.MachineNaming.AllowManualSuffixEdit;
            ApplyOobeState(settings.Oobe);
            ApplyAiComponentRemovalState(settings.AiComponentRemoval);
            ApplyAppxRemovalState(settings.AppxRemoval);
        }
        finally
        {
            isApplyingState = false;
        }
    }

    private void SaveState()
    {
        if (isApplyingState || isRefreshingOperatingSystemSelectionOptions || HasMachineNamePrefixValidationError)
        {
            return;
        }

        isSavingState = true;
        try
        {
            configurationStateService.UpdateCustomization(new CustomizationSettings
            {
                MachineNaming = BuildMachineNamingSettings(),
                Oobe = BuildOobeSettings(),
                AiComponentRemoval = BuildAiComponentRemovalSettings(),
                AppxRemoval = BuildAppxRemovalSettings()
            });
            configurationStateService.UpdateOperatingSystemSelection(BuildOperatingSystemSelectionSettings());
        }
        finally
        {
            isSavingState = false;
        }
    }

    private void RefreshLocalizedText()
    {
        PageTitle = localizationService.GetString("CustomizationPage_Title.Text");
        PageDescription = localizationService.GetString("Customization.PageDescription");
        MachineNamingHeader = localizationService.GetString("Customization.MachineNamingHeader");
        MachineNamingDescription = localizationService.GetString("Customization.MachineNamingDescription");
        MachineNamingEnableText = localizationService.GetString("Customization.MachineNamingEnableLabel");
        MachineNamingPrefixLabel = localizationService.GetString("Customization.MachineNamingPrefixLabel");
        MachineNamingPrefixDescription = localizationService.GetString("Customization.MachineNamingPrefixDescription");
        MachineNamingAutoGenerateText = localizationService.GetString("Customization.MachineNamingAutoGenerateLabel");
        MachineNamingAutoGenerateDescription = localizationService.GetString("Customization.MachineNamingAutoGenerateDescription");
        MachineNamingAllowManualSuffixEditText = localizationService.GetString("Customization.MachineNamingAllowManualSuffixLabel");
        MachineNamingAllowManualSuffixEditDescription = localizationService.GetString("Customization.MachineNamingAllowManualSuffixDescription");
        RefreshOperatingSystemSelectionLocalizedText();
        RefreshOobeLocalizedText();
        RefreshAiComponentRemovalLocalizedText();
        RefreshAppxRemovalLocalizedText();
        OnPropertyChanged(nameof(MachineNamePrefixValidationMessage));
        OnPropertyChanged(nameof(HasMachineNamePrefixValidationError));
        OnPropertyChanged(nameof(MachineNamePrefixValidationVisibility));
    }

    private void OnLanguageChanged(object? sender, ApplicationLanguageChangedEventArgs e)
    {
        RefreshLocalizedText();
    }

    private void OnConfigurationStateChanged(object? sender, EventArgs e)
    {
        if (isSavingState)
        {
            return;
        }

        ApplyState(
            configurationStateService.Current.Customization,
            configurationStateService.Current.OperatingSystemSelection);
    }

    private static string NormalizePrefix(string? value)
    {
        return ComputerNameTemplate.NormalizePrefix(value?.Trim());
    }

    private MachineNamingSettings BuildMachineNamingSettings()
    {
        string normalizedPrefix = NormalizePrefix(MachineNamePrefix);
        string? prefix = string.IsNullOrWhiteSpace(normalizedPrefix)
            ? null
            : normalizedPrefix;

        return new MachineNamingSettings
        {
            IsEnabled = IsMachineNamingEnabled,
            Prefix = IsMachineNamingEnabled ? prefix : null,
            AutoGenerateName = IsMachineNamingEnabled && MachineNameAutoGenerate,
            AllowManualSuffixEdit = !IsMachineNamingEnabled || AllowManualSuffixEdit
        };
    }
}
